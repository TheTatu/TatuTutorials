package com.reusingclasses;

public class Employee {
	public String Name;
	public String Department;
	public String city;
	public int Floor;
	
public Employee() {}
	
	public Employee(String Name, String Department, String city,int Floor) {
		this.Name = Name;
		this.Department = Department;
		this.city = city;
		this.Floor = Floor;
	}

}
